    private void calcBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcBTNActionPerformed
        // TODO add your handling code here:
        String grade = "";
        // handle exception with try catch block
        try{
        //get input from textfield
        String name= nameTF.getText();
        String matric = matricTF.getText();  
        String sem = semCB.getSelectedItem().toString();
        double carry = Double.parseDouble(carryTF.getText());
        double finalExam = Double.parseDouble(finalTF.getText());

        //handle exception for carry marks
        if (carry > 60 || carry <0)
            throw new Exception("Error. Carry Marks out of range!");
        
        //handle exception for final exam marks
        if (finalExam > 40 || finalExam<0)
            throw new Exception("Error. Final out of range!");
        //create Student object 
        Student s = new Student(name,matric,sem,carry,finalExam,grade);
        s.computeGrade(); //calculate marks and identify grade
        list.add(s); //add student object to array list
        //display grade and total marks
        gradeTF.setText(s.getGrade());
        totalTF.setText(String.valueOf(s.getTotalMarks()));
        } 
   
        catch(Exception e){
           JOptionPane.showMessageDialog(null,e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);       
        }

    }//GEN-LAST:event_calcBTNActionPerformed

    private void displayBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBTNActionPerformed
        // TODO add your handling code here:
                //format header
       String s=String.format("%-40s%-20s%-15s%-10s%-10s\n","Name","Matric No", "Semester", "Marks","Grade");
        //format each element in arrayList
        for (Student list1 : list) {
            list1.computeGrade();
            s += list1.toString();
        }
        //display all elements at text area
        outputTA.setText(s);

    }//GEN-LAST:event_displayBTNActionPerformed

