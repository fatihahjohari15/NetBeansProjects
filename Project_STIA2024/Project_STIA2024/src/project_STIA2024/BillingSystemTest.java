    // perform binary search
    private void binarySearch(String input, String process) throws IOException{
        loadFileToArray();
        String action = process;
        
        if (billArrayList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty List");
        } else {
            try {
                index = Collections.binarySearch(billArrayList,
                        new BillingSystem(null, input, null, null, null, 0, null, 0, 0, 0, 0, 0), new AccNoCompare());
                if (action.equals("search")){
                    loadArrayToTable(index);
                }
                else if (action.equals("edit") || action.equals("delete")){
                    loadArrayToTextfield(index, action);
                }
                
            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, "Data Not Found");
                if (action.equals("edit")){
                    jTextSearchEdit.setText("");
                    jTextEditBillDate.setText("");
                    jTextEditAccNo.setText("");
                    jTextEditInvoiceNo.setText("");
                    jTextEditUserName.setText("");
                    jTextEditUserAddress.setText("");
                    jTextEditOverdueAmt.setText("");
                    jTextEditMeterNo.setText("");
                    jTextEditOldMeter.setText("");
                    jTextEditNewMeter.setText("");
                }
            }
        }
        jTextSearchData.setText("");
    }
    
