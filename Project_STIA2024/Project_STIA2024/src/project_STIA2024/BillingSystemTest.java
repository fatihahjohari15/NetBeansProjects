    // sort data from userInputData.txt and rewrite in ASCENDING on userOutputData.txt
    private void sortTextFile() throws FileNotFoundException, IOException {
        BufferedReader reader = new BufferedReader(new FileReader("userInputData.txt"));

        ArrayList<BillingSystem> billRecords = new ArrayList<BillingSystem>();

        String currentLine = reader.readLine();

        while (currentLine != null) {
            String[] billDetail = currentLine.split(" # ");

            billDate = billDetail[0];
            accountNo = billDetail[1];
            invoiceNo = billDetail[2];
            userName = billDetail[3];
            userAddress = billDetail[4];
            overdueAmt = Double.valueOf(billDetail[5]);
            meterNo = billDetail[6];
            oldMeter = Double.valueOf(billDetail[7]);
            newMeter = Double.valueOf(billDetail[8]);
            totalUsage = Double.valueOf(billDetail[9]);
            totalCharge = Double.valueOf(billDetail[10]);
            totalBill = Double.valueOf(billDetail[11]);

            billRecords.add(new BillingSystem(billDate, accountNo, invoiceNo, userName, userAddress, overdueAmt, meterNo, oldMeter, newMeter, totalUsage, totalCharge, totalBill));

            currentLine = reader.readLine();
        }

        Collections.sort(billRecords, new AccNoCompare());

        BufferedWriter writer = new BufferedWriter(new FileWriter("userOutputData.txt"));

        for (BillingSystem bill : billRecords) {
            writer.write(bill.billDate + " , " + bill.accountNo + " , " + bill.invoiceNo + " , " + bill.userName + " , " + bill.userAddress + " , " + bill.overdueAmt
                    + " , " + bill.meterNo + " , " + bill.oldMeter + " , " + bill.newMeter + " , " + bill.totalUsage + " , " + bill.totalCharge + " , " + bill.totalBill);
            writer.newLine();
        }

        reader.close();
        writer.close();
    }
    
    // load data from userOutputData.txt into jTable
    private void loadFileToTable() {
        jTable1.clearSelection();
        try {
            sortTextFile();
            BufferedReader br = new BufferedReader(new FileReader("userOutputData.txt"));
            String[] columnsName = {"Tarikh Bil", "No Akaun", "No Invois Cukai", "Nama Pengguna", "Alamat Pengguna",
                "Tunggakan", "No Meter", "Bacaan Meter Dahulu", "Bacaan Meter Sekarang", "Jumlah Unit Kegunaan",
                "Caj Semasa", "Jumlah Bayaran Bil"};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setColumnIdentifiers(columnsName);

            Object[] tableLines = br.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(" , ");
                model.addRow(dataRow);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BillingSystemTest.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(BillingSystemTest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
